    void tampil_tabel_barang (){
        DefaultTableModel model= new DefaultTableModel();
        model.addColumn("kode barang");
        model.addColumn("nama barang");
        model.addColumn("stok");
        model.addColumn("harga jual");
        model.addColumn("nama beli");
        try{
            String sql="SELECT * FROM barang";
            java.sql.Connection conn=koneksi.ConfigDB();
            java.sql.Statement statement=conn.createStatement();
            java.sql.ResultSet resultSet=statement.executeQuery(sql);
            while(resultSet.next()){
                model.addRow(new Object[]{resultSet.getString("kode_barang"),resultSet.getString("nama_barang"),resultSet.getString("stok"),resultSet.getString("harga_jual"),resultSet.getString("harga_beli")});
            }
           ttabelbarang.setModel(model);
        }catch (Exception e){
            e.printStackTrace();
            }
        }
    
