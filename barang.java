    private void ttabelbarangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ttabelbarangMouseClicked
        // TODO add your handling code here:
        int baris= ttabelbarang.rowAtPoint(evt.getPoint());
        String kode_barang= ttabelbarang.getValueAt(baris, 0).toString();
        String nama_barang= ttabelbarang.getValueAt(baris, 1).toString();
        String stock = ttabelbarang.getValueAt(baris, 2).toString();
        String harga_beli= ttabelbarang.getValueAt(baris, 3).toString();
        String harga_jual= ttabelbarang.getValueAt(baris, 4).toString();
         idbarang.setText(kode_barang);
         tnamab.setText(nama_barang);
         ts.setText(stock);
         thb.setText(harga_beli);
         thj.setText(harga_jual);
         idbarang.setEditable(false);
         
         
       
    }//GEN-LAST:event_ttabelbarangMouseClicked

